            <h2 class="alert-heading">About Sports Social Rank</h2><h5>
            Sports Social Rank is a ranking of sports teams and players based on twitter followers being updated
            every 15 minutes.
          </br>Twitter data is the best social data for tracking fan behavior. According to comScore,
             “Fans uniquely turn to Twitter during live events as their second — and primary — screen to learn what’s happening in real time. Across key live entertainment and sporting events throughout the world, Twitter, on average, sees a +4.1% lift in unique visitors while other social platforms, in aggregate, see no significant change. The difference is even more pronounced with tentpole sports events. During the 2017 Super Bowl, Twitter saw a +19% lift in unique visitors (versus average Sunday traffic) while other social platforms, in aggregate, saw a decline in unique visitors.”
</h5>
Currently the site shows data from these categories NFL, NBA, MLB, College Football,College Basketball, MLS,
NFL Players,  Premier League, and FCS Football. We will continue to update this list of categories in the future.
